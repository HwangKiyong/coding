package coding.method;

public class InputDemo {
	public static void main(String[] args) {   
		System.out.println(args.length); //args변수가 배열이 몇개 담을 수 있는지 출력하는것이다.
	}
}

//main 메소드는 약속되어있는 메소드이다.
/*String[] args  
String 은 문자열
[] 배열
args 변수

parameter는 메소드로 들어오는 입력값이다.
void 는 main이란 메소드는 출력값이 존재하지않는다 라는 뜻이다.
*/
package coding;

public class EqualDemo 
{
	public static void main(String[] args)
	{
		System.out.println(1==2);
		System.out.println(1==1);
		System.out.println("one"=="two"); //동등비교 연산자는 숫자에만 사용하는것은 아니다.
		System.out.println("one"=="one"); //문자에도 가능하다.
		//true와false는 조건문에서 사용되게 될것이다.
	}
}
